#ยกตัวอย่างที่ผิดTuplesในรูปแบบการเพิ่มข้อมูลไปยังตัวแปรnames2ที่เก็บข้อมูลแบบTuplesที่ผิดพลาดซึ่งไม่สามารถใช้appendแบบListได้ ผลที่ได้จะError
